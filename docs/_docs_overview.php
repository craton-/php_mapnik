<?php

namespace Mapnik;

/**
 * Introduction
 *
 * mod_mapnik is a set of PHP bindings for the mapnik (mapnik.org) map rendering
 * library. Mapnik provides an easy-to-use interface for making graphical
 * representations of a variety of common GIS (Geographical Information Systems)
 * data formats, with many options to allow designers and cartographers to render
 * the best-looking results.
 * mod_mapnik gives the PHP programmer full access to all functionality the
 * mapnik library provides.
 *
 */


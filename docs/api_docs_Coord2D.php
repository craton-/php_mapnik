<?php

namespace Mapnik;

/**
 * A simple class to represent a point in a Cartesian plane.
 */
class Coord2D
{
    /**
     * Default constructor, initializes both X and Y components to 0.
     */
    Coord2D() {}
    /**
     * Constructor that initializes the X and Y components to the passed in values.
     */
    Coord2D(double $x, double $y) {}
    /**
     * Returns the X coordinate.
     */
    function getX() {}
    /**
     * Returns the Y coordinate.
     */
    function getY() {}
}
